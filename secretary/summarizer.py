"""Формирование протокола совещания через LLM."""
import logging
from core.app_time import format_history_timestamp
from core.llm_client import get_llm

logger = logging.getLogger(__name__)

SYSTEM_PROMPT = """Ты — секретарь совещания. На основе транскрипта составь структурированный протокол.
Формат ответа (Markdown):

## Протокол совещания

**Дата:** (извлеки из текста или укажи «не указана»)

**Участники:** (список, если можно определить из текста)

### Повестка
- пункты повестки

### Основные обсуждаемые вопросы
- краткое изложение по каждому вопросу

### Принятые решения
- нумерованный список решений

### Поручения
| № | Поручение | Ответственный | Срок |
|---|-----------|---------------|------|
| 1 | ... | ... | ... |

Пиши на русском языке. Если информации нет — укажи «не определено»."""


def build_protocol(transcript: str, filename: str = "") -> str:
    """Генерация протокола из транскрипта."""
    llm = get_llm()
    prompt = (
        f"Имя файла записи: {filename}\n"
        f"Дата обработки: {format_history_timestamp()}\n\n"
        f"Транскрипт совещания:\n\n{transcript}"
    )
    logger.info("Генерация протокола, длина транскрипта=%d", len(transcript))
    return llm.generate(prompt, system_prompt=SYSTEM_PROMPT)
